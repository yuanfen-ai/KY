/**
 * 获取键盘模式标题
 */
const getModeTitle = () => {
  if (isNumberMode.value) return '数字/符号';
  if (isPinyinMode.value) return '拼音输入';
  return isUpperCase.value ? '大写字母' : '小写字母';
};

/**
 * 处理按键
 */
const handleKey = (key: string) => {
  // 切换到数字模式
  if (key === '123') {
    isNumberMode.value = true;
    isPinyinMode.value = false;
    return;
  }

  // 从数字模式切换回字母模式（小写）
  if (key === 'abc') {
    isNumberMode.value = false;
    isUpperCase.value = false;
    isPinyinMode.value = false;
    return;
  }

  // 拼音模式切换
  if (key === '拼音') {
    isPinyinMode.value = !isPinyinMode.value;
    if (isPinyinMode.value) {
      currentPinyin.value = '';
      candidates.value = [];
    }
    return;
  }

  // 大小写切换
  if (key === '大小写') {
    isUpperCase.value = !isUpperCase.value;
    return;
  }

  // 删除键
  if (key === '删除') {
    if (isPinyinMode.value && currentPinyin.value.length > 0) {
      currentPinyin.value = currentPinyin.value.slice(0, -1);
      updateCandidates();
    } else {
      deleteChar();
    }
    return;
  }

  // 完成键
  if (key === '完成') {
    if (isPinyinMode.value && currentPinyin.value) {
      confirmPinyin();
    }
    close();
    return;
  }

  // 空格键
  if (key === '空格') {
    if (isPinyinMode.value && currentPinyin.value) {
      confirmPinyin();
    } else {
      insertText(' ');
    }
    return;
  }

  // 拼音模式处理
  if (isPinyinMode.value) {
    // 只接受字母
    if (/^[a-zA-Z]$/.test(key)) {
      currentPinyin.value += key.toLowerCase();
      updateCandidates();
    }
    return;
  }

  // 普通字母或数字
  insertText(key);
};

/**
 * 删除字符
 */
const deleteChar = () => {
  if (props.inputRef) {
    const pos = props.inputRef.selectionStart || 0;
    const val = props.inputRef.value;
    if (pos > 0) {
      props.inputRef.value = val.slice(0, pos - 1) + val.slice(pos);
      props.inputRef.setSelectionRange(pos - 1, pos - 1);
      props.inputRef.dispatchEvent(new Event('input', { bubbles: true }));
    }
    props.inputRef.focus();
  }
};

/**
 * 更新候选词
 */
const updateCandidates = () => {
  const pinyin = currentPinyin.value;
  if (!pinyin) {
    candidates.value = [];
    return;
  }

  // 查找匹配的汉字
  let results: string[] = [];
  
  // 精确匹配
  if (pinyinMap[pinyin]) {
    results = pinyinMap[pinyin];
  }
  
  // 模糊匹配（当前缀）
  if (results.length === 0) {
    for (const key in pinyinMap) {
      if (key.startsWith(pinyin)) {
        results = results.concat(pinyinMap[key]);
        break;
      }
    }
  }

  // 限制候选词数量
  candidates.value = results.slice(0, 9);
};

/**
 * 选择候选字
 */
const selectCandidate = (char: string) => {
  insertText(char);
  currentPinyin.value = '';
  candidates.value = [];
};

/**
 * 确认拼音输入（将拼音作为英文输入）
 */
const confirmPinyin = () => {
  if (currentPinyin.value) {
    insertText(currentPinyin.value);
    currentPinyin.value = '';
    candidates.value = [];
  }
};

/**
 * 插入文本
 */
const insertText = (text: string) => {
  if (props.inputRef) {
    const pos = props.inputRef.selectionStart || 0;
    const val = props.inputRef.value;
    props.inputRef.value = val.slice(0, pos) + text + val.slice(pos);
    props.inputRef.setSelectionRange(pos + text.length, pos + text.length);
    props.inputRef.dispatchEvent(new Event('input', { bubbles: true }));
    props.inputRef.focus();
  }
};

/**
 * 关闭键盘
 */
const close = () => {
  emit('update:visible', false);
  emit('close');
  if (props.inputRef) {
    props.inputRef.blur();
  }
  // 重置拼音状态
  currentPinyin.value = '';
  candidates.value = [];
  isPinyinMode.value = false;
};

// 监听 visible 变化，重置键盘模式
watch(() => props.visible, (newVal) => {
  if (newVal) {
    isNumberMode.value = false;
    isUpperCase.value = false;
    isPinyinMode.value = false;
    currentPinyin.value = '';
    candidates.value = [];
  }
});
